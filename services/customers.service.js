const boom = require('@hapi/boom');
const { models } = require('../libs/sequelize');

class CustomerService {

  constructor() {}

  async find() {
    const rta = await models.Customer.findAll({
      include: ["user"] //de esta forma se incluye la relacion con la tabla user
    });
    return rta;
  }

  async findOne(id) {
    const user = await models.Customer.findByPk(id);
    if (!user) {
      throw boom.notFound('customer not found');
    }
    return user;
  }

  async create(data) {
    // const newUser = await models.User.create(data.user); //Para crear primero el usuario
    const newCustomer = await models.Customer.create(data,{
      include: ["user"] //de esta forma se incluye la relacion con la tabla user automaticamente
      // userId :newUser.id //Se crea el customer con el id del usuario
    });
    return newCustomer;
  }

  async update(id, changes) {
    const model = await this.findOne(id);
    const rta = await model.update(changes);
    return rta;
  }

  async delete(id) {
    const model = await this.findOne(id);
    await model.destroy();
    return { rta: true };
  }

}

module.exports = CustomerService;
